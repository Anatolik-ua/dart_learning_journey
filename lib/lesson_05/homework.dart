/// Додатково: для вихідних (6, 7) вивести “Вихідний день 🎉”.

void task7() {
  final dayNumber = 6;

  switch (dayNumber) {
    case 1:
      print('Понеділок');
    case 2:
      print('Вівторок');
    case 3:
      print('Середа');
    case 4:
      print('Четвер');
    case 5:
      print('П’ятниця');
    case 6:
    case 7:
      final name = dayNumber == 6 ? 'Субота' : 'Неділя';
      print('$name — Вихідний день 🎉');
    default:
      print('Невірний номер дня');
  }
}

/// Завдання 8: Цикли for
/// Виведіть всі числа від 1 до 10.
/// Виведіть тільки парні числа.
/// Обчисліть суму чисел від 1 до 10.

void task8() {
  print('Всі числа від 1 до 10:');
  int i;
  for (i = 1; i <= 10; i++) {
    print(i);
  }

  print('\nТільки парні числа:');
  for (i = 1; i <= 10; i++) {
    if (i % 2 == 0) {
      print(i);
    }
  }

  var sum = 0;
  for (i = 1; i <= 10; i++) {
    sum += i;
  }
  print('\nСума всіх чисел від 1 до 10: $sum');
}

/// Завдання 9: Цикл while
/// Почніть із числа 10.
///
/// Виводьте числа, зменшуючи їх на 1, поки не досягнете 0.
/// Якщо число парне — вивести “Парне число: …”,
/// якщо непарне — “Непарне число: …”.

void task9() {
  var number = 10;

  while (number > 0) {
    if (number % 2 == 0) {
      print('Парне число: $number');
    } else {
      print('Непарне число: $number');
    }
    number--;
  }
}

